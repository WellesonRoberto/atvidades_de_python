# Desenvolva um programa que receba dois valores.Na sequência,
# verifique se a equação 4a+3=b é verdadeira.Caso seja falsa,
# o programa encerra sua execução

a = int(input("Digite o primeiro valor: "))
b = int(input("Digite o segundo valor: "))

if 4*a + 3 == b:
    print("Equação verdadeira!")
